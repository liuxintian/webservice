package com.omt.config.entity;

public class SysConfigVO {
	private String cfgName;
	private String cfgAlias;
	private String cfgValue;
	private String cfgDefaultValue;
	
	public String getCfgName() {
		return cfgName;
	}
	public void setCfgName(String cfgName) {
		this.cfgName = cfgName;
	}
	public String getCfgAlias() {
		return cfgAlias;
	}
	public void setCfgAlias(String cfgAlias) {
		this.cfgAlias = cfgAlias;
	}
	public String getCfgValue() {
		return cfgValue;
	}
	public void setCfgValue(String cfgValue) {
		this.cfgValue = cfgValue;
	}
	public String getCfgDefaultValue() {
		return cfgDefaultValue;
	}
	public void setCfgDefaultValue(String cfgDefaultValue) {
		this.cfgDefaultValue = cfgDefaultValue;
	}
}
